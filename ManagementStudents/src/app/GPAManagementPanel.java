    private void bntSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntSaveActionPerformed
        // TODO add your handling code here:
        int flas = 0;
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtStudentID, sb, "Không để trống mã sinh viên");
        if (sb.length() > 0) {
            MessageDialog.showErrorDialog(parent, sb.toString(), "Lỗi");
            return;
        }
        BangDiem bd = new BangDiem();
        List<SinhVien> list = SinhVienDao.findAll1();
        for (SinhVien sv : list) {
            if (sv.getMaSinhVien().equals(txtStudentID.getText())) {
                flas = 1;
                txtName.setText(sv.getHoTen());
                bd.setMaSinhVien(txtStudentID.getText());
                bd.setThanhPhan1(Float.parseFloat(txtTP1.getText()));
                bd.setThanhPhan2(Float.parseFloat(txtTP2.getText()));
                bd.setCuoiKy(Float.parseFloat(txtCK.getText()));
            }
        }
        if (flas == 0) {
            MessageDialog.showMessageDialog(parent, "Mã sinh viên không tồn tại", "Thông báo");
        } else {
            try {
                BangDiemDao.insert(bd);
                float gpa = (bd.getThanhPhan1() * 1) / 10 + (bd.getThanhPhan2() * 2) / 10 + (bd.getCuoiKy() * 7) / 10;
                lblGpa.setText(String.valueOf(gpa));
                MessageDialog.showMessageDialog(parent, "Bảng điểm đã được lưu", "Thông báo");
                loadDataToTable(); // cập nhật lại dữ liệu chứa trong bảng.
            } catch (Exception e) {
                MessageDialog.showErrorDialog(parent, e.getMessage(), "Lỗi");
            }
        }


    }//GEN-LAST:event_bntSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtStudentID, sb, "Không để trống mã sinh viên");

        if (sb.length() > 0) {
            MessageDialog.showErrorDialog(parent, sb.toString(), "Lỗi");
            return;
        }
        BangDiem bd = new BangDiem();
        bd.setThanhPhan1(Float.parseFloat(txtTP1.getText()));
        bd.setThanhPhan2(Float.parseFloat(txtTP2.getText()));
        bd.setCuoiKy(Float.parseFloat(txtCK.getText()));
        bd.setMaSinhVien(txtStudentID.getText());
        try {
            BangDiemDao.update(bd);
            float gpa = (bd.getThanhPhan1() * 1) / 10 + (bd.getThanhPhan2() * 2) / 10 + (bd.getCuoiKy() * 7) / 10;
            lblGpa.setText(String.valueOf(gpa));
            MessageDialog.showMessageDialog(parent, "Bảng điểm đã được cập nhật", "Thông báo");
            loadDataToTable();
        } catch (Exception e) {
            MessageDialog.showErrorDialog(parent, e.getMessage(), "Lỗi");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

