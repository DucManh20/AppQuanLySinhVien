    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        List<BangDiem> listbd = BangDiemDao.findAll1();
        SinhVien sv = new SinhVien();
        Collections.sort(listbd, (BangDiem bd1, BangDiem bd2) -> (int) (((bd2.getThanhPhan1() * 1) / 10 + (bd2.getThanhPhan2() * 2) / 10 + (bd2.getCuoiKy() * 7) / 10) * 10
                - ((bd1.getThanhPhan1() * 1) / 10 + (bd1.getThanhPhan2() * 2) / 10 + (bd1.getCuoiKy() * 7) / 10) * 10));
        tblModel.setRowCount(0);
        for (BangDiem bd : listbd) {
            sv.setMaSinhVien(bd.getMaSinhVien());
            SinhVienDao.search(sv);

            float gpa = (bd.getThanhPhan1() * 1) / 10 + (bd.getThanhPhan2() * 2) / 10 + (bd.getCuoiKy() * 7) / 10;
            tblModel.addRow(new Object[]{
                bd.getMaSinhVien(), sv.getHoTen(), bd.getThanhPhan1(), bd.getThanhPhan2(), bd.getCuoiKy(), gpa
            });
        }
        tblModel.fireTableDataChanged(); // đảm bảo dữ liệu sau khi thay đổi được hiển thị trên bảng
    }//GEN-LAST:event_jButton1ActionPerformed


